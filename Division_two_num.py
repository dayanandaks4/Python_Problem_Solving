# Dividing Two number.
# div two number we get a float output
def div(num1,num2):
  n1 = num1
  n2 = num2
  div = float(n1 * n2)
  print(f"Diff: {div}")
div(10,5)

# div two number we get a int output
def div(num1,num2):
  n1 = num1
  n2 = num2
  div = int(n1 * n2)
  print(f"Diff: {div}")
div(10,5)

